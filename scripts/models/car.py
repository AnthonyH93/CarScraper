# Class to describe a car
class Car:
    def __init__(self, manufacturer, model, first_year_produced, last_year_produced, engine, transmission, weight):
        self.manufacturer = manufacturer
        self.model = model
        self.first_year_produced = first_year_produced
        self.last_year_produced = last_year_produced
        self.engine = engine
        self.transmission = transmission
        self.weight = weight
